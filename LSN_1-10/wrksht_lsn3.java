public class wrksht_lsn3
{
  public static void main(String args[])
  {
      String s1 = "Allan Alda";
      String s2 = "John Wayne";
      String s3 = "Gregory Peck";
      int s1len = s1.length();
      int s2len = s2.length();
      int s3len = s3.length();
      System.out.println(s1.substring(2,s1len - 3));
      System.out.println(s2.substring(2,s2len - 3));
      System.out.println(s3.substring(2,s3len - 3));
  }

}
